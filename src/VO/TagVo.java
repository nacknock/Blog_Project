package VO;

public class TagVo {
	private int tag_id;
	private String tag_name;
	private int tag_p_id;
	public int getTag_id() {
		return tag_id;
	}
	public void setTag_id(int tag_id) {
		this.tag_id = tag_id;
	}
	public String getTag_name() {
		return tag_name;
	}
	public void setTag_name(String tag_name) {
		this.tag_name = tag_name;
	}
	public int getTag_p_id() {
		return tag_p_id;
	}
	public void setTag_p_id(int tag_p_id) {
		this.tag_p_id = tag_p_id;
	}
}